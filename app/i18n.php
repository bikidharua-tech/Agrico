// Minimal i18n helper.
// Locale codes: en (English), hi (Hindi), or (Odia)

function supported_locales(): array {
    return ['en', 'hi', 'or'];
}

function current_locale(): string {
    $loc = $_SESSION['locale'] ?? ($_COOKIE['locale'] ?? 'en');
    return in_array($loc, supported_locales(), true) ? $loc : 'en';
}

function set_locale(string $loc): void {
    if (!in_array($loc, supported_locales(), true)) return;
    $_SESSION['locale'] = $loc;
    // 180 days
    setcookie('locale', $loc, [
        'expires' => time() + 180 * 24 * 60 * 60,
        'path' => '/',
        'httponly' => false,
        'samesite' => 'Lax',
    ]);
}

function lang_url(string $loc): string {
    // Preserve current path/query and only change lang=...
    $uri = $_SERVER['REQUEST_URI'] ?? '/';
    $parts = parse_url($uri);
    $path = $parts['path'] ?? '/';
    $query = [];
    if (!empty($parts['query'])) parse_str($parts['query'], $query);
    $query['lang'] = $loc;
    return $path . '?' . http_build_query($query);
}

